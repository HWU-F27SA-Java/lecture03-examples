
public class SpecialAssignment {

	public static void main(String[] args) {
		int n = 10;
		System.out.println("n : " + n);
		n += 2; 
		System.out.println(" n+=2 : " + n);
		n -= 2;
		System.out.println(" n-=2 : " + n);
		n *= 2;
		System.out.println(" n*=2 : " + n);
		n /= 2;
		System.out.println(" n/=2 : " + n);
		n++;
		System.out.println(" n++ : " + n);
		n--;
		System.out.println(" n-- : " + n);

	}

}
